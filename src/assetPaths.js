const collectAssetPaths = (value) => {
  if (typeof value === 'string') {
    return /[/.](png|jpe?g|svg|webp)$/i.test(value) || value.includes('/')
      ? [value]
      : [];
  }

  if (Array.isArray(value)) {
    return value.flatMap((entry) => collectAssetPaths(entry));
  }

  if (value && typeof value === 'object') {
    return Object.values(value).flatMap((entry) => collectAssetPaths(entry));
  }

  return [];
};

